import java.util.*;

public class number {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int i = sc.nextInt();
        int k = 1;
        for( int j =1; j<=10; j++){
            k=i*j;
           
            System.out.println(k);
        }
        
    }
    
}
